//We can delete an element from an array using the pop() method,
var array1 = [1, 2, 3, 4, 5];
array1.pop();
console.log(array1);

//Another method to remove an element from an array is using shift() method. It will remove the first element of an array and return it.
var array2 = [1, 2, 3, 4, 5];
array2.shift();
console.log(array2);